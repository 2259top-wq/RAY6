// Synastry compatibility score calculator
export function calculateCompatibility(aspects) {
  let score = 70; // Base score
  let harmonyCount = 0;
  let challengeCount = 0;

  aspects.forEach(asp => {
    if (asp.aspectName === '三分相' || asp.aspectName === '六分相') {
      harmonyCount++;
      if (['Sun', 'Moon', 'Venus'].includes(asp.p1) && ['Sun', 'Moon', 'Venus'].includes(asp.p2)) {
        score += 4;
      } else {
        score += 2;
      }
    } else if (asp.aspectName === '四分相' || asp.aspectName === '對分相') {
      challengeCount++;
      if (['Saturn', 'Pluto', 'Mars'].includes(asp.p1) || ['Saturn', 'Pluto', 'Mars'].includes(asp.p2)) {
        score -= 3;
      } else {
        score -= 1.5;
      }
    } else if (asp.aspectName === '合相') {
      if (['Sun', 'Moon', 'Venus'].includes(asp.p1) && ['Sun', 'Moon', 'Venus'].includes(asp.p2)) {
        score += 5;
        harmonyCount++;
      } else if (['Saturn', 'Pluto'].includes(asp.p1) || ['Saturn', 'Pluto'].includes(asp.p2)) {
        score -= 2;
        challengeCount++;
      } else {
        score += 2.5;
      }
    }
  });

  score = Math.max(40, Math.min(99, score));
  return {
    score: Math.round(score),
    harmonyCount,
    challengeCount,
    grade: score >= 85 ? '天作之合（靈魂契合度極高，非常適合長久發展）' : score >= 75 ? '和諧融洽（緣分深厚，日常相處舒適且相互扶持）' : score >= 60 ? '互補磨合（性格互補，但在生活觀念上需要攜手修行）' : '宿命挑戰（宿命糾纏強烈，相處伴隨較多成長課題）'
  };
}

